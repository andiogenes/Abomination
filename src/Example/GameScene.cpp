void GameScene::update()
{
    Scene::update();

    player->horizontalMovementStart();
    player->horizontalMovementEnd(map->isCollidingWithRect(player->getBufferX(), player->getY(), 64, 64));

    player->verticalMovementStart();
    player->verticalMovementEnd(map->isCollidingWithRect(player->getX(), player->getBufferY(), 64, 64));

    if (player->getX() <= 368)
        cameraPosX = 0;
    else
        cameraPosX = 368 - player->getX();

    set->setCulling(-cameraPosX / 64, 0);

    if (player->getLifes() == 0 && notDead) {
        music->stop();
        deathMusic->play();
        notDead = false;
    }

    if (!notDead) {
        deathTimer += 0.01;

        if (deathTimer > 7) {
            deathMusic->stop();
            Frog::FG::engine->setScene(new GameScene());
            delete this;
        }
    }

    if (box != nullptr &&
            Frog::BoundingBox::checkCollision(player->getX(),
                                              player->getY(),
                                              64, 64,
                                              box->getX(),
                                              box->getY(), 64, 64)) {
        music->stop();
        victoryMusic->play();

        removeEntity(box);
        box = nullptr;

        isVictory = true;
    }

    if (isVictory) {
        victoryTimer += 0.01;

        if (victoryTimer > 6) {
            victoryMusic->stop();
            Frog::FG::engine->setScene(new MenuScene());
            delete this;
        }
    }
}

void GameScene::onDraw()
{
    // Отрисовка фона
    Frog::FG::setCamera(0, 0);
    Frog::FG::graphics->setColor(Frog::Color(114, 201, 207));
    Frog::FG::graphics->drawQuad(Frog::GraphicsComponent::DrawMode::Fill, 0, 0, 805, 605);

    Frog::FG::setCamera(cameraPosX, -186);

    Scene::onDraw();

    Frog::FG::setCamera(0, 0);
    // Отрисовка полоски здоровья
    int lifes = player->getLifes();

    for (int i = 0; i < lifes / 2; i++) {
        Frog::FG::graphics->drawImage(&healthImage, 25 + i * 32, 25);
    }

    if (lifes % 2 != 0) {
        Frog::FG::graphics->drawImage(&healthImage, 25 + (lifes / 2) * 32, 25, 0, 0,
                                      healthImage.getWidth() / 2, healthImage.getHeight());
    }

    // Вывод сообщения "ВЫ УМЕРЛИ"
    if (!notDead) {
        auto opacity = 6 * deathTimer / 7.;
        if (opacity > 1) opacity = 1;

        Frog::FG::graphics->setColor(Frog::Color(0, 0, 0, 255 * opacity));
        Frog::FG::graphics->drawQuad(Frog::GraphicsComponent::DrawMode::Fill, 0, 250, 800, 100);
        Frog::FG::graphics->setColor(Frog::Color(125, 0, 0, 255 * opacity));
        Frog::FG::graphics->drawText("YOU DIED", 380, 300);
    }

    if (isVictory) {
        auto opacity = 5 * victoryTimer / 6.;
        if (opacity > 1) opacity = 1;

        Frog::FG::graphics->setColor(Frog::Color(255, 255, 255, 255 * opacity));
        Frog::FG::graphics->drawQuad(Frog::GraphicsComponent::DrawMode::Fill, 0, 250, 800, 100);
        Frog::FG::graphics->setColor(Frog::Color(0, 125, 45, 255 * opacity));
        Frog::FG::graphics->drawText("BOX PICKED UP", 380, 300);
    }
    Frog::FG::setCamera(0, 0);
}

void GameScene::keyPressed(int keyCode)
{
    if (keyCode == Qt::Key_Z && canJump) {
        player->jump();
        canJump = false;
    }
}

void GameScene::keyReleased(int keyCode)
{
    if (keyCode == Qt::Key_Z)
        canJump = true;
}
